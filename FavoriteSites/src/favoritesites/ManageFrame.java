    private void setSeason(String season) {
       Enumeration<AbstractButton> buttons = buttonGroup2.getElements();
        for (AbstractButton button : Collections.list(buttons)) {
            if (button.getText().equals(season)) {
                button.setSelected(true);
                break;
            }
        }
    }
    
    private void setComboBoxRating(int rating) {
    
        for (int i = 0; i < jComboBoxRating2.getItemCount(); i++) {
            if (Integer.parseInt(jComboBoxRating2.getItemAt(i).toString()) == rating) {
                // Eşleşen öğeyi seçin
                jComboBoxRating2.setSelectedIndex(i);
                break;
            } 
        }
}
    
