    private void jTextFieldDisplayVisitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextFieldDisplayVisitActionPerformed
        Integer id = Integer.parseInt(jTextFieldDisplayVisit.getText());
        try {
            statement = connection.createStatement();
            String sql = "select * from visits where visit_id  =  " +id;   
            resultSet1 = statement.executeQuery(sql);
            if(resultSet1.next()) {
                jTextFieldCountryName2.setText(resultSet1.getString("country_name"));
                jTextFieldCityName2.setText(resultSet1.getString("city_name"));
                jTextFieldYear2.setText(String.valueOf(resultSet1.getInt("year_visited")));
                String season = resultSet1.getString("season_visited");
                setSeason(season);
                jTextFieldFeature2.setText(resultSet1.getString("best_feature"));
                jTextFieldComment2.setText(resultSet1.getString("user_comment"));
                Integer rating = resultSet1.getInt("rating");
                setComboBoxRating(rating);
            } else {
                
                JOptionPane.showMessageDialog(this, "No records found for visit ID: " +id , "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextFieldDisplayVisitActionPerformed

